# Simulated observer (if no audio)
# ---------------------------------
def simulated_2afc_correct(p: ExperimentParams, level_db: float) -> bool:
    """
    A smooth psychometric function for 2AFC detection.
    Probability correct = gamma + (1 - gamma - lambda) * (1 - exp(-(10^((L - t)/20))^beta))
    For 2AFC, gamma = 0.5.
    """
    gamma = 0.5
    lapse = p.sim_lapse_rate
    # Convert dB to linear relative to threshold
    x = 10 ** ((level_db - p.sim_threshold_db) / 20.0)
    # Weibull-like form
    pc = gamma + (1 - gamma - lapse) * (1 - math.exp(-(x ** p.sim_slope_beta)))
    return random.random() < pc


