def db_to_amplitude(db: float) -> float:
    """Convert dBFS to linear amplitude (0..1)."""
    return 10 ** (db / 20.0)


def cosine_ramp(signal: "np.ndarray", ramp_samples: int) -> "np.ndarray":
    """Apply a raised-cosine ramp up and down to avoid clicks."""
    if ramp_samples <= 0:
        return signal
    ramp = 0.5 - 0.5 * np.cos(np.linspace(0, math.pi, ramp_samples))
    env = np.ones_like(signal)
    env[:ramp_samples] *= ramp
    env[-ramp_samples:] *= ramp[::-1]
    return signal * env


def make_tone(f_hz: float, dur_s: float, sr: int, amp: float, ramp_s: float) -> "np.ndarray":
    t = np.arange(int(dur_s * sr)) / sr
    wave = np.sin(2 * np.pi * f_hz * t) * amp
    ramp_n = int(ramp_s * sr)
    return cosine_ramp(wave, min(ramp_n, len(wave)//2))


def play_audio(left: "np.ndarray", right: Optional["np.ndarray"] = None, sr: int = 44100):
    """Play mono or stereo buffer (blocks until done)."""
    if right is None:
        buf = left.astype(np.float32)
    else:
        buf = np.column_stack([left, right]).astype(np.float32)
    sd.play(buf, sr, blocking=True)


def prompt_user(prompt: str) -> str:
    """Prompt user and return a single lowercase character (like '1' or '2')."""
    sys.stdout.write(prompt)
    sys.stdout.flush()
    while True:
        ch = sys.stdin.readline().strip().lower()
        if ch:
            return ch


# ---------------------------------
# Staircase logic: 1-up / 2-down
# ---------------------------------
@dataclass
class StaircaseState:
    current_db: float
    trial_index: int = 0
    reversals: int = 0
    last_direction: Optional[int] = None   # +1 up, -1 down, None at start
    reversal_levels: List[float] = field(default_factory=list)
    consecutive_correct: int = 0
    history: List[Tuple[int, int, bool, float]] = field(default_factory=list)  # (trial, signal_interval, correct?, level_db)


class OneUpTwoDown:
    def __init__(self, params: ExperimentParams):
        self.p = params
        clamped = max(min(params.start_level_db, params.max_level_db), params.min_level_db)
        if clamped != params.start_level_db:
            print(f"[Note] start_level_db clamped to {clamped} dB")
        self.state = StaircaseState(current_db=clamped)

    def step(self, correct: bool):
        """
        Update staircase based on correctness.
        - If correct: increase 'consecutive_correct'. When it reaches 2, decrease level (DOWN), reset counter.
        - If incorrect: immediately increase level (UP), reset consecutive_correct.
        """
        s = self.state
        direction = None

        if correct:
            s.consecutive_correct += 1
            if s.consecutive_correct >= 2:
                # DOWN (harder)
                old = s.current_db
                s.current_db = max(s.current_db - self.p.step_down_db, self.p.min_level_db)
                direction = -1
                s.consecutive_correct = 0
        else:
            s.consecutive_correct = 0
            # UP (easier)
            old = s.current_db
            s.current_db = min(s.current_db + self.p.step_up_db, self.p.max_level_db)
            direction = +1

        # reversal check
        if direction is not None and s.last_direction is not None and direction != s.last_direction:
            s.reversals += 1
            s.reversal_levels.append(old)
        if direction is not None:
            s.last_direction = direction

    def done(self) -> bool:
        return (self.state.reversals >= self.p.max_reversals) or (self.state.trial_index >= self.p.max_trials)

    def threshold_estimate(self) -> Optional[float]:
        n = self.p.reversals_for_threshold
        if len(self.state.reversal_levels) >= n:
            return sum(self.state.reversal_levels[-n:]) / n
        return None


