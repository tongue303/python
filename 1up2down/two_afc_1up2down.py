def run_trial(params: ExperimentParams, staircase: OneUpTwoDown, csv_writer: csv.writer) -> None:
    s = staircase.state
    s.trial_index += 1

    # Randomize which interval has the signal
    signal_interval = random.choice([1, 2])

    # Prepare audio if possible
    if AUDIO_OK and not params.FORCE_SIMULATION:
        amp = db_to_amplitude(s.current_db)
        tone = make_tone(params.frequency_hz, params.interval_duration_s, params.sample_rate, amp, params.ramp_s)
        silence = np.zeros_like(tone)

        # Interval 1
        if signal_interval == 1:
            buf1 = tone
            buf2 = silence
        else:
            buf1 = silence
            buf2 = tone

        # Play intervals with ISI
        print(f"\nTrial {s.trial_index}: presenting intervals...")
        print("Listen: Interval 1")
        play_audio(buf1, sr=params.sample_rate)
        time.sleep(params.isi_s)
        print("Listen: Interval 2")
        play_audio(buf2, sr=params.sample_rate)

        # Collect response
        resp_prompt = f"Which interval had the tone? Press {params.response_keys[0]} (1st) or {params.response_keys[1]} (2nd): "
        resp_key = prompt_user(resp_prompt)
        if resp_key not in params.response_keys:
            print(f"Invalid key '{resp_key}'. Counting as incorrect.")
            correct = False
        else:
            chosen = 1 if resp_key == params.response_keys[0] else 2
            correct = (chosen == signal_interval)

    else:
        # Simulated observer (no audio or forced)
        correct = simulated_2afc_correct(params, s.current_db)
        print(f"\n[SIM] Trial {s.trial_index}: level {s.current_db:.1f} dBFS -> {'correct' if correct else 'incorrect'} (signal interval={signal_interval})")

    # Feedback & log
    if params.feedback:
        print("✓ Correct!" if correct else "✗ Incorrect.")
    csv_writer.writerow([s.trial_index, signal_interval, int(correct), f"{s.current_db:.3f}"])

    # Update staircase
    staircase.step(correct)

    # Show current status
    thr = staircase.threshold_estimate()
    if thr is not None:
        print(f"Current threshold estimate (mean of last {staircase.p.reversals_for_threshold} reversals): {thr:.2f} dBFS")
    print(f"Next level: {staircase.state.current_db:.2f} dBFS | reversals: {staircase.state.reversals}")


